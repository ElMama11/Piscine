int	ft_is_prime(int nb)
{
	int	nbr;

	nbr = 2;
	if (nb < 2)
		return (0);
	while (nbr <= nb / nbr)
	{
		if (nb % nbr == 0)
			return (0);
		nbr++;
	}
	return (1);
}
